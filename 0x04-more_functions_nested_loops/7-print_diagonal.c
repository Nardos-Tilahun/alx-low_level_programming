#include "main.h"

/**
 * print_diagonal -prints the numbers, from 0 to 9, followed by a new line
 * @n: that indicate how many times we will draw
 */
void print_diagonal(int n)
{
	int i, j;

	if (n > 0)
	{
		for (i = 0; i < n; i++)
		{
			for (j = 0; j < i; j++)
			{
				_putchar(' ');
			}
			_putchar('\\');
			_putchar('\n');
		}
	}
	else
	{
		_putchar('\n');
	}
}
